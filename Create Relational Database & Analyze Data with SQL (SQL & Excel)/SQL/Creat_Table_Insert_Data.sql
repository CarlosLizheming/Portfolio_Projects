---Create Table
CREATE TABLE Coach
(
  Coach_ID VARCHAR(10) NOT NULL PRIMARY KEY,
  Coach_Name VARCHAR(50) NOT NULL,
  Nationality VARCHAR(5) NOT NULL,
  FOREIGN KEY (Nationality) REFERENCES Country(Country_ID)
)
GO

CREATE TABLE Match_Info
(
  [Year] INT NOT NULL,
  Round_ID INT NOT NULL,
  Match_ID INT NOT NULL PRIMARY KEY,
  [Date] DATE NOT NULL,
  [Time] Datetime NOT NULL,
  Stage VARCHAR(50) NOT NULL,
  Stadium_ID VARCHAR(10) NOT NULL,
  Referee_ID VARCHAR(10) NOT NULL,
  Assistant1_ID VARCHAR(10) NOT NULL,
  Assistant2_ID VARCHAR(10) NOT NULL,
  Attendance INT NOT NULL,
  FOREIGN KEY ([Year]) REFERENCES World_Cup(Year),
  FOREIGN KEY (Stadium_ID) REFERENCES Stadium(Stadium_ID),
  FOREIGN KEY (Referee_ID) REFERENCES Referee(Referee_ID),
  FOREIGN KEY (Assistant1_ID) REFERENCES Referee(Referee_ID),
  FOREIGN KEY (Assistant2_ID) REFERENCES Referee(Referee_ID)
);
GO
Alter Table Match_Info ALter Column [Time] Time(0)
GO

CREATE TABLE Match_Score
(
  Match_ID INT NOT NULL PRIMARY KEY,
  Home_Team VARCHAR(5) NOT NULL,
  Home_Team_Goals INT NOT NULL,
  Away_Team_Goals INT NOT NULL,
  Away_Team VARCHAR(5) NOT NULL,
  Win_Conditions VARCHAR(100),
  Half_Time_Home_Goals INT NOT NULL,
  Half_Time_Away_Goals INT NOT NULL,  
  FOREIGN KEY (Match_ID) REFERENCES Match_Info(Match_ID),
  FOREIGN KEY (Home_Team) REFERENCES Country(Country_ID),
  FOREIGN KEY (Away_Team) REFERENCES Country(Country_ID)
)
GO

CREATE TABLE Players_Each_Match
(
  Match_ID INT NOT NULL,
  Player_Name VARCHAR(50) NOT NULL,
  Coach_ID VARCHAR(10) NOT NULL,
  Position VARCHAR(5),
  Shirt_Number INT NOT NULL,
  Line_Up VARCHAR(5) NOT NULL,
  Country_ID VARCHAR(5) NOT NULL,
  Event VARCHAR(50),
  PRIMARY KEY (Player_Name, Match_ID),
  FOREIGN KEY (Coach_ID) REFERENCES Coach(Coach_ID),
  FOREIGN KEY (Country_ID) REFERENCES Country(Country_ID),
  FOREIGN KEY (Match_ID) REFERENCES Match_Info(Match_ID)
)
GO

